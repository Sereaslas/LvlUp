    /**
     * Este metodo se encarga de cargar todas la etiquetas al panel central
     *
     * @param panel
     */
    private void cargarEtiquetas(JPanel panel) {
        panel.setLayout(new GridLayout(2, 4, 0, 0));
        for (Juego j : juegos.obtener()) {
            EtiquetaJuegoUI portada = new EtiquetaJuegoUI(j, juegosParaCliente, juegos, clientes, this);
            this.panelCentralJuegos.add(portada.getEtiqueta(contadorJuegos));
        }
    }

    /**
     * Este metodo se encarga de borrar todas las etiquetas del panel central
     *
     * @return
     */
    private JPanel borraretiquetas() {
        JPanel panelleno = panelCentralJuegos;
        this.panelCentralJuegos.removeAll();
        return panelleno;
    }

