    /**
     * Este metodo se encarga de buscar el juego que se introduce dentro del
     * textField
     *
     * @param evt
     */
    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String nombre = this.textBuscar.getText();
        for (Juego j : juegos.obtener()) {
            if (j.getNombre().equalsIgnoreCase(nombre) || j.getDesarrollador().equalsIgnoreCase(nombre) || j.getGenero().equalsIgnoreCase(nombre)) {
                aux = borraretiquetas();
                EtiquetaJuegoUI portada = new EtiquetaJuegoUI(j, juegosParaCliente, juegos, clientes, this);
                panelCentralJuegos.setLayout(new FlowLayout());
                panelCentralJuegos.setOpaque(false);
                panelCentralJuegos.add(portada.getEtiqueta(contadorJuegos));
                panelCentralJuegos.updateUI();
                validarEtiquetas = true;
            } else if (nombre.equals("") && validarEtiquetas) {
                borraretiquetas();
                cargarEtiquetas(aux);
                panelCentralJuegos = aux;
                validarEtiquetas = false;
                panelCentralJuegos.updateUI();
            }
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

